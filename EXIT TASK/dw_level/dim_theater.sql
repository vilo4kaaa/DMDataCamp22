CREATE TABLE DIM_THEATER(
    THEATER_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,     
    THEATER_NAME               VARCHAR2(20),
    THEATER_CAPACITY           NUMBER,
    NUMBER_OF_ROOMS            NUMBER,
    THEATER_ADDRESS            VARCHAR2(100),
    THEATER_TELEPHONE          VARCHAR2(15),
    CONSTRAINT "PK_T.DIM_THEATER" PRIMARY KEY(THEATER_ID) 
);


 