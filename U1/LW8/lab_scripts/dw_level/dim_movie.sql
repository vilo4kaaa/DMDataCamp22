CREATE TABLE DIM_MOVIE(
    MOVIE_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,     
    MOVIE_NAME             DATE,
    MOVIE_YEAR          VARCHAR2(4),
    MOVIE_STUDIO        VARCHAR2(50),
    MOVIE_COUNTRY       VARCHAR2(50),
    MOVIE_LANGIUAGE     VARCHAR2(50),
    MOVIE_GENRE         VARCHAR2(50),
    MOVIE_DURATION      NUMBER,
    AGE_RESTRICTIONS    NUMBER,
    CONSTRAINT "PK_T.DIM_MOVIE" PRIMARY KEY(MOVIE_ID) 
);